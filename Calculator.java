public class Calculator {
    
    public int divide(int num1, int num2) 
    {
        return num1 / num2;
    }
    
    public int suma (int num1, int num2) {
        return num1 + num2;
    }
    
    public int resta (int num1, int num2) {
        return num1 - num2;
    }
    
    public int multiplica (int num1, int num2) {
        return num1 * num2;
    }
}

